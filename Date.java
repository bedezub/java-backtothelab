public class Date {
    
    private int Day;
    private int Month;
    private int Year;
    
    public Date(int d, int m, int y) {
    
        Day = d;
        Month = m;
        Year = y;
    }
    
    public int getDay() {
        return Day;
    }
    
    public int getMonth() {
        return Month;
    }
    
    public int getYear() {
        return Year;
    }
   
}